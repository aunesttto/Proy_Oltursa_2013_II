package bean;

public class Telefono {
	protected int cod_per;
	protected int cod_tel;
	protected String nro_tel;
	protected String est_tel;
	public int getCod_per() {
		return cod_per;
	}
	public void setCod_per(int cod_per) {
		this.cod_per = cod_per;
	}
	public int getCod_tel() {
		return cod_tel;
	}
	public void setCod_tel(int cod_tel) {
		this.cod_tel = cod_tel;
	}
	public String getNro_tel() {
		return nro_tel;
	}
	public void setNro_tel(String nro_tel) {
		this.nro_tel = nro_tel;
	}
	public String getEst_tel() {
		return est_tel;
	}
	public void setEst_tel(String est_tel) {
		this.est_tel = est_tel;
	}
	
}
