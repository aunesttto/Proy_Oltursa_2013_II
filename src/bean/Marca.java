package bean;

public class Marca {
	protected int cod_mar;
	protected String nom_mar;
	protected String des_mar;
	protected String est_mar;
	public int getCod_mar() {
		return cod_mar;
	}
	public void setCod_mar(int cod_mar) {
		this.cod_mar = cod_mar;
	}
	public String getNom_mar() {
		return nom_mar;
	}
	public void setNom_mar(String nom_mar) {
		this.nom_mar = nom_mar;
	}
	public String getDes_mar() {
		return des_mar;
	}
	public void setDes_mar(String des_mar) {
		this.des_mar = des_mar;
	}
	public String getEst_mar() {
		return est_mar;
	}
	public void setEst_mar(String est_mar) {
		this.est_mar = est_mar;
	}	
}
