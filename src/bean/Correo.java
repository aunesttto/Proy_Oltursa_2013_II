package bean;

public class Correo {
	protected int cod_per;
	protected int cod_cor;
	protected String nom_cor;
	protected String est_cor;
	public int getCod_per() {
		return cod_per;
	}
	public void setCod_per(int cod_per) {
		this.cod_per = cod_per;
	}
	public int getCod_cor() {
		return cod_cor;
	}
	public void setCod_cor(int cod_cor) {
		this.cod_cor = cod_cor;
	}
	public String getNom_cor() {
		return nom_cor;
	}
	public void setNom_cor(String nom_cor) {
		this.nom_cor = nom_cor;
	}
	public String getEst_cor() {
		return est_cor;
	}
	public void setEst_cor(String est_cor) {
		this.est_cor = est_cor;
	}	
}
