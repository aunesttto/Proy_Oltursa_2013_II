package bean;

public class Cliente {
	private int cod_per;
	private String usu_cli;
	private String pass_cli;
	private String nro_tar_cli;
	private String est_cli;
	public int getCod_per() {
		return cod_per;
	}
	public void setCod_per(int cod_per) {
		this.cod_per = cod_per;
	}
	public String getUsu_cli() {
		return usu_cli;
	}
	public void setUsu_cli(String usu_cli) {
		this.usu_cli = usu_cli;
	}
	public String getPass_cli() {
		return pass_cli;
	}
	public void setPass_cli(String pass_cli) {
		this.pass_cli = pass_cli;
	}
	public String getNro_tar_cli() {
		return nro_tar_cli;
	}
	public void setNro_tar_cli(String nro_tar_cli) {
		this.nro_tar_cli = nro_tar_cli;
	}
	public String getEst_cli() {
		return est_cli;
	}
	public void setEst_cli(String est_cli) {
		this.est_cli = est_cli;
	}	
}
