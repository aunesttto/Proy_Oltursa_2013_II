package bean;

public class Submodulo {
	protected int cod_sub;
	protected int cod_mod;
	protected int cod_rol;
	protected String nom_sub;
	protected String det_sub;
	protected String est_sub;
	public int getCod_sub() {
		return cod_sub;
	}
	public void setCod_sub(int cod_sub) {
		this.cod_sub = cod_sub;
	}
	public int getCod_mod() {
		return cod_mod;
	}
	public void setCod_mod(int cod_mod) {
		this.cod_mod = cod_mod;
	}
	public int getCod_rol() {
		return cod_rol;
	}
	public void setCod_rol(int cod_rol) {
		this.cod_rol = cod_rol;
	}
	public String getNom_sub() {
		return nom_sub;
	}
	public void setNom_sub(String nom_sub) {
		this.nom_sub = nom_sub;
	}
	public String getDet_sub() {
		return det_sub;
	}
	public void setDet_sub(String det_sub) {
		this.det_sub = det_sub;
	}
	public String getEst_sub() {
		return est_sub;
	}
	public void setEst_sub(String est_sub) {
		this.est_sub = est_sub;
	}
}
