package bean;

public class Modelo {
	protected int cod_mod;
	protected int cod_mar;
	protected String nom_mod;
	protected String des_mod;
	protected String est_mod;
	public int getCod_mod() {
		return cod_mod;
	}
	public void setCod_mod(int cod_mod) {
		this.cod_mod = cod_mod;
	}
	public int getCod_mar() {
		return cod_mar;
	}
	public void setCod_mar(int cod_mar) {
		this.cod_mar = cod_mar;
	}
	public String getNom_mod() {
		return nom_mod;
	}
	public void setNom_mod(String nom_mod) {
		this.nom_mod = nom_mod;
	}
	public String getDes_mod() {
		return des_mod;
	}
	public void setDes_mod(String des_mod) {
		this.des_mod = des_mod;
	}
	public String getEst_mod() {
		return est_mod;
	}
	public void setEst_mod(String est_mod) {
		this.est_mod = est_mod;
	}
	
}
