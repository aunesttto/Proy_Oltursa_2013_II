package bean;

public class Ciudad {
	protected int cod_pais;
	protected int cod_ciudad;
	protected String nom_ciu;
	protected String des_ciu;
	protected String est_ciu;
	public int getCod_pais() {
		return cod_pais;
	}
	public void setCod_pais(int cod_pais) {
		this.cod_pais = cod_pais;
	}
	public int getCod_ciudad() {
		return cod_ciudad;
	}
	public void setCod_ciudad(int cod_ciudad) {
		this.cod_ciudad = cod_ciudad;
	}
	public String getNom_ciu() {
		return nom_ciu;
	}
	public void setNom_ciu(String nom_ciu) {
		this.nom_ciu = nom_ciu;
	}
	public String getDes_ciu() {
		return des_ciu;
	}
	public void setDes_ciu(String des_ciu) {
		this.des_ciu = des_ciu;
	}
	public String getEst_ciu() {
		return est_ciu;
	}
	public void setEst_ciu(String est_ciu) {
		this.est_ciu = est_ciu;
	}
		
}
