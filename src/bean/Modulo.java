package bean;

public class Modulo {
	private int cod_mod;
	private int cod_rol;
	private String nom_mod;
	private String det_mod;
	private String esr_mod;
	public int getCod_mod() {
		return cod_mod;
	}
	public void setCod_mod(int cod_mod) {
		this.cod_mod = cod_mod;
	}
	public int getCod_rol() {
		return cod_rol;
	}
	public void setCod_rol(int cod_rol) {
		this.cod_rol = cod_rol;
	}
	public String getNom_mod() {
		return nom_mod;
	}
	public void setNom_mod(String nom_mod) {
		this.nom_mod = nom_mod;
	}
	public String getDet_mod() {
		return det_mod;
	}
	public void setDet_mod(String det_mod) {
		this.det_mod = det_mod;
	}
	public String getEsr_mod() {
		return esr_mod;
	}
	public void setEsr_mod(String esr_mod) {
		this.esr_mod = esr_mod;
	}	
}
