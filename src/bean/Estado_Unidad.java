package bean;

public class Estado_Unidad {
	protected int cod_est_uni;
	protected String nom_est;
	protected String dest_est;
	protected String est_est;
	public int getCod_est_uni() {
		return cod_est_uni;
	}
	public void setCod_est_uni(int cod_est_uni) {
		this.cod_est_uni = cod_est_uni;
	}
	public String getNom_est() {
		return nom_est;
	}
	public void setNom_est(String nom_est) {
		this.nom_est = nom_est;
	}
	public String getDest_est() {
		return dest_est;
	}
	public void setDest_est(String dest_est) {
		this.dest_est = dest_est;
	}
	public String getEst_est() {
		return est_est;
	}
	public void setEst_est(String est_est) {
		this.est_est = est_est;
	}
	
}
