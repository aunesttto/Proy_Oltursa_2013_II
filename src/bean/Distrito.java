package bean;

public class Distrito {
	protected int cod_pais;
	protected int cod_ciudad;
	protected int cod_dis;
	protected String nom_dis;
	protected String est_dis;
	public int getCod_pais() {
		return cod_pais;
	}
	public void setCod_pais(int cod_pais) {
		this.cod_pais = cod_pais;
	}
	public int getCod_ciudad() {
		return cod_ciudad;
	}
	public void setCod_ciudad(int cod_ciudad) {
		this.cod_ciudad = cod_ciudad;
	}
	public int getCod_dis() {
		return cod_dis;
	}
	public void setCod_dis(int cod_dis) {
		this.cod_dis = cod_dis;
	}
	public String getNom_dis() {
		return nom_dis;
	}
	public void setNom_dis(String nom_dis) {
		this.nom_dis = nom_dis;
	}
	public String getEst_dis() {
		return est_dis;
	}
	public void setEst_dis(String est_dis) {
		this.est_dis = est_dis;
	}

}
