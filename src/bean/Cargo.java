package bean;

public class Cargo {
	protected int cod_car;
	protected String nom_car;
	protected String des_car;
	protected int est_car;
	public int getCod_car() {
		return cod_car;
	}
	public void setCod_car(int cod_car) {
		this.cod_car = cod_car;
	}
	public String getNom_car() {
		return nom_car;
	}
	public void setNom_car(String nom_car) {
		this.nom_car = nom_car;
	}
	public String getDes_car() {
		return des_car;
	}
	public void setDes_car(String des_car) {
		this.des_car = des_car;
	}
	public int getEst_car() {
		return est_car;
	}
	public void setEst_car(int est_car) {
		this.est_car = est_car;
	}	
}
