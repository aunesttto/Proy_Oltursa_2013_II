package bean;

public class Pais {
	protected int cod_pais;
	protected String nom_pais;
	protected String est_pais;
	public int getCod_pais() {
		return cod_pais;
	}
	public void setCod_pais(int cod_pais) {
		this.cod_pais = cod_pais;
	}
	public String getNom_pais() {
		return nom_pais;
	}
	public void setNom_pais(String nom_pais) {
		this.nom_pais = nom_pais;
	}
	public String getEst_pais() {
		return est_pais;
	}
	public void setEst_pais(String est_pais) {
		this.est_pais = est_pais;
	}
	
	
}
